#!/usr/bin/env python3

import alchemy.transmutation as tr
from alchemy.transmutation.basic import lead_to_gold, stone_to_gem


def test():
    print("\nTesting Absolute Imports (from basic.py):")
    print(f"lead_to_gold(): {lead_to_gold()}")
    print(f"stone_to_gem(): {stone_to_gem()}")

    print("\nTesting Relative Imports (from advanced.py):")
    print(f"philosophers_stone(): {tr.philosophers_stone()}")
    print(f"elixir_of_life(): {tr.elixir_of_life()}")

    print("\nTesting Package Access:")
    print(f"alchemy.transmutation.lead_to_gold(): {tr.lead_to_gold()}")
    print(f"alchemy.transmutation.philosophers_stone(): "
          f"{tr.philosophers_stone()}")


if __name__ == "__main__":
    print("=== Pathway Debate Mastery ===")
    test()
    print("\nBoth pathways work! Absolute: clear, Relative: concise")
